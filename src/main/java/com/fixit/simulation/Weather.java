package com.fixit.simulation;

/**
 * Enum for the weather condition of the simulation.
 */
public enum Weather {
  /**
   * Represents a sunny condition in this simulation.
   */
  SUNNY,
  /**
   * Represents a rainy condition in this simulation.
   */
  RAINY,
  /**
   * Represents a foggy condition in this simulation.
   */
  FOGGY,
}
